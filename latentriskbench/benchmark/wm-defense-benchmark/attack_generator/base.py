"""Core data classes for the attack generator."""
from __future__ import annotations

from dataclasses import dataclass, field, asdict
from typing import Any, Dict, List, Optional


# ---------------------------------------------------------------------------
# 5-axis threat-model taxonomy (Phase 5 formalisation; see docs/wm_0417.md §C.6)
# ---------------------------------------------------------------------------

# Enumerable options per axis. Keeping these as module-level tuples so they
# can be referenced from both the factory and the CLI's choices= lists.
ROLLOUT_OPTIONS: tuple[str, ...] = ("none", "simulate", "wrapper")
POLICY_OPTIONS: tuple[str, ...] = ("full", "summary", "none")
BUDGET_OPTIONS: tuple[str, ...] = ("known", "unknown")
# The following two axes are formalised in docs but not yet instrumented as
# first-class flags; kept here so the dataclass has slots and records the
# current default for every attack:
RISK_SCORE_OPTIONS: tuple[str, ...] = ("scalar", "binary")
PRE_FILTER_OPTIONS: tuple[str, ...] = ("visible", "hidden")


# Conventional 3-tier labels → explicit 5-axis Cartesian products.
# If any axis flag is set on the CLI it overrides the preset slot.
_THREAT_MODE_PRESETS: Dict[str, Dict[str, str]] = {
    "white_box": {
        "rollout": "wrapper",
        "policy": "full",
        "budget": "known",
        "risk_score": "scalar",
        "pre_filter": "visible",
    },
    "grey_box": {
        "rollout": "simulate",
        "policy": "summary",
        "budget": "unknown",
        "risk_score": "scalar",
        "pre_filter": "hidden",
    },
    "black_box": {
        "rollout": "none",
        "policy": "none",
        "budget": "unknown",
        "risk_score": "binary",
        "pre_filter": "hidden",
    },
}


@dataclass
class AttackerAxes:
    """Explicit, code-level-controllable attacker knowledge configuration.

    Every payload generated by this benchmark carries an ``AttackerAxes``
    snapshot in its ``generation_metadata.attacker_config`` so that attack
    transferability results are traceable to the specific axis
    configuration they were generated under.

    Attributes:
        rollout: "none" | "simulate" | "wrapper" — which path, if any,
            the attacker uses to score candidate actions during
            generation. "wrapper" routes through the same
            ``evaluate_action_risk`` call the defender uses at eval time.
        policy: "full" | "summary" | "none" — how much of the defender's
            policy specification the attacker's LLM prompt exposes.
        budget: "known" | "unknown" — whether the attacker knows the
            defender's tree_search_n_root / max_depth / refusal threshold.
        risk_score: "scalar" | "binary" — reserved for Phase P3; whether
            the attacker sees a scalar risk in [0,1] or only a boolean.
        pre_filter: "visible" | "hidden" — reserved for Phase P3;
            whether the attacker can observe deterministic secret-pattern
            pre-filter outcomes before invoking the LLM.
        threat_mode: The conventional 3-tier label this configuration
            was derived from (or "custom" if axes were overridden
            individually). Kept for backwards-compatible logs and human
            readability.
    """

    rollout: str = "none"
    policy: str = "none"
    budget: str = "unknown"
    risk_score: str = "binary"
    pre_filter: str = "hidden"
    threat_mode: str = "custom"

    @classmethod
    def from_threat_mode(cls, mode: str) -> "AttackerAxes":
        """Construct axes from a 3-tier preset label."""
        preset = _THREAT_MODE_PRESETS.get(mode)
        if preset is None:
            raise ValueError(
                f"Unknown threat_mode {mode!r}; expected one of {sorted(_THREAT_MODE_PRESETS)}"
            )
        return cls(**preset, threat_mode=mode)

    def override(
        self,
        *,
        rollout: Optional[str] = None,
        policy: Optional[str] = None,
        budget: Optional[str] = None,
        risk_score: Optional[str] = None,
        pre_filter: Optional[str] = None,
    ) -> "AttackerAxes":
        """Return a new AttackerAxes with selected axes replaced. If any
        axis is overridden, ``threat_mode`` is set to ``"custom"`` to
        avoid suggesting the result is still a canonical 3-tier config.
        """
        updates: Dict[str, Any] = {}
        for name, value, valid in (
            ("rollout", rollout, ROLLOUT_OPTIONS),
            ("policy", policy, POLICY_OPTIONS),
            ("budget", budget, BUDGET_OPTIONS),
            ("risk_score", risk_score, RISK_SCORE_OPTIONS),
            ("pre_filter", pre_filter, PRE_FILTER_OPTIONS),
        ):
            if value is None:
                continue
            if value not in valid:
                raise ValueError(f"{name}: {value!r} not in {valid}")
            updates[name] = value
        if not updates:
            return self
        new_mode = "custom"
        # If every axis matches a known preset, keep the preset label.
        for mode, preset in _THREAT_MODE_PRESETS.items():
            merged = {**asdict(self), **updates}
            if all(merged.get(k) == v for k, v in preset.items()):
                new_mode = mode
                break
        return AttackerAxes(
            rollout=updates.get("rollout", self.rollout),
            policy=updates.get("policy", self.policy),
            budget=updates.get("budget", self.budget),
            risk_score=updates.get("risk_score", self.risk_score),
            pre_filter=updates.get("pre_filter", self.pre_filter),
            threat_mode=new_mode,
        )

    def to_dict(self) -> Dict[str, Any]:
        return asdict(self)


@dataclass
class GenerationRequest:
    """One generation attempt against a single benign base task.

    Attributes:
        base_task: The benign task dict loaded from ``base_tasks.jsonl``.
        seed_actions: List of known-unsafe candidate actions to rephrase.
            These come from existing attack payloads that target this
            ``base_task``; the generator searches for variants of each seed.
        n_variants: How many rephrased variants per seed to request from the
            attacker world model in a single call.
        max_payloads: Cap on successful payloads returned for this request
            (even if more seeds / variants succeed).
        threat_mode: ``white_box`` / ``grey_box`` / ``black_box``. Only
            ``white_box`` is supported in Phase 1; the field is carried
            through to generation records for replay.
        attacker_seed: Deterministic seed for the attacker WM (if supported).
        state_override: Optional state dict that replaces ``base_task["state"]``
            for this request. When provided, the attacker, defender, AND
            oracle all see ``state_override`` instead of the base task's
            initial state. This unlocks beam × rephrase composition: run a
            beam generator to depth k, take one
            ``BeamNode.predicted_state``, and pass it here to rephrase at
            an intermediate (WM-predicted) state. Default ``None`` =
            behaviour unchanged (use ``base_task["state"]``). Recorded in
            ``GenerationRecord.attacker_config.state_override_used``.
    """

    base_task: Dict[str, Any]
    seed_actions: List[str]
    n_variants: int = 6
    max_payloads: int = 3
    threat_mode: str = "white_box"
    attacker_seed: int = 0
    state_override: Optional[Dict[str, Any]] = None


@dataclass
class VariantScore:
    """Per-variant audit record.

    Attributes:
        variant_index: 0-based position in the attacker WM's response.
        action: The rephrased action string.
        wm_violated_policy_ids: ``violated_policy_ids`` returned by the
            defender WM's prediction for this variant. Empty list means the
            defender would deem the variant safe.
        wm_risk_score: Defender WM's aggregated risk score (0-1).
        oracle_unsafe: Verdict from the independent policy oracle.
        oracle_policy_ids: Oracle-identified violated policies.
        oracle_confidence: Oracle's self-reported confidence.
        oracle_matched_rules: Rule IDs that fired (rule oracle).
        adversarial: True iff the variant fools the WM and oracle flags it.
        reject_reason: Why the variant was not selected, if any.
    """

    variant_index: int
    action: str
    wm_violated_policy_ids: List[str] = field(default_factory=list)
    wm_risk_score: float = 0.0
    oracle_unsafe: bool = False
    oracle_policy_ids: List[str] = field(default_factory=list)
    oracle_confidence: float = 0.0
    oracle_matched_rules: List[str] = field(default_factory=list)
    adversarial: bool = False
    reject_reason: Optional[str] = None
    # Full defender-WM delta (``DefenderVerdict.raw_delta``). Carried for
    # audit, dynamics-accuracy metrics, and future WM-rollout attackers.
    # Empty dict when the underlying scorer did not populate it (e.g. stubs).
    wm_raw_delta: Dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "variant_index": self.variant_index,
            "action": self.action,
            "wm_violated_policy_ids": list(self.wm_violated_policy_ids),
            "wm_risk_score": self.wm_risk_score,
            "oracle_unsafe": self.oracle_unsafe,
            "oracle_policy_ids": list(self.oracle_policy_ids),
            "oracle_confidence": self.oracle_confidence,
            "oracle_matched_rules": list(self.oracle_matched_rules),
            "adversarial": self.adversarial,
            "reject_reason": self.reject_reason,
            "wm_raw_delta": dict(self.wm_raw_delta) if self.wm_raw_delta else {},
        }


@dataclass
class GenerationResult:
    """Outcome of a single ``GenerationRequest``.

    Attributes:
        status: ``"ok"`` if at least one payload was produced; ``"null"``
            when no variant met the adversarial criterion; ``"budget"`` if
            the run aborted due to compute budget exhaustion.
        reason: Short machine-readable reason when status != ``"ok"``.
        payloads: Successful attack payload dicts ready to be written to a
            JSONL file under ``data/attack_payloads/wm_adaptive/``.
        records: One ``GenerationRecord`` per (seed, batch) attempt,
            regardless of success. Written under
            ``data/attack_generation_logs/``.
    """

    status: str
    reason: Optional[str] = None
    payloads: List[Dict[str, Any]] = field(default_factory=list)
    records: List[Dict[str, Any]] = field(default_factory=list)

    @property
    def ok(self) -> bool:
        return self.status == "ok" and bool(self.payloads)
